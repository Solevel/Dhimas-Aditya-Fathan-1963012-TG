    private void btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn1ActionPerformed
        // TODO add your handling code here:
        try {
            if(nis.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Mohon Masukan nis");
                nis.requestFocus();
            } else if (nama.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Mohon Masukan Nama");
                nama.requestFocus();
            } else if(ttl.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Mohon Masukan Tanggal");
                ttl.requestFocus();
            } else if(alamat.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Mohon Masukan Alamat");
                alamat.requestFocus();
            } else if(a_sekolah.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Mohon Masukan Asal Sekolah");
                a_sekolah.requestFocus();
            } else if(n_un.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Mohon Masukan Nilai UN");
                n_un.requestFocus();
            } else {
                String[] field = {"nis","nama","ttl","alamat","a_sekolah","n_un"};
                String[] isi = {nis.getText(),nama.getText(),ttl.getText(),alamat.getText(),a_sekolah.getText(),n_un.getText()};
                new Fungsi().SimpanDinamis("peserta_baru", field, isi);
                new Fungsi().setJudulTabel(tbl1, Judul);
                new Fungsi().tampilIsiTabel(tbl1, SQL, Judul);
            }
        } catch (Exception e){
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_btn1ActionPerformed


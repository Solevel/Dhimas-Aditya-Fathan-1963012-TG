    private void btn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn2ActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "UPDATE peserta_baru SET nis = '"+nis.getText()+"', nama = '"+nama.getText()+"', ttl = '"+ttl.getText()+"',alamat= '"+alamat.getText()+"',a_sekolah= '"+a_sekolah.getText()+"',n_un= '"+n_un.getText()+"' WHERE nis = '"+nis.getText()+"'";
            Connection conn=(Connection)Koneksi.koneksiDB();
            PreparedStatement st=conn.prepareStatement(sql);
            st.executeUpdate();
            new Fungsi().setJudulTabel(tbl1, Judul);
            new Fungsi().tampilIsiTabel(tbl1, SQL, Judul);
            JOptionPane.showMessageDialog(null, "Data Berhasil diubah");
        } catch (Exception e){
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_btn2ActionPerformed

